    private void BTN_AgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BTN_AgregarMouseClicked
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/tecnoauto", "root", "");
            PreparedStatement pst = cn.prepareStatement("insert into egresos values(?,?,?,?,?,?)");

            String TITULO = titulo.getText().trim();
            String FECHA = ((JTextField) fecha.getDateEditor().getUiComponent()).getText();
            String DESCRIPCION = descripcion.getText().trim();
            String PROVEDOR = provedor.getText().trim();
            String MONTO = monto.getText().trim();

            if (descripcion.equals("")) {
                DESCRIPCION = null;
            }

            if (provedor.equals("")) {
                PROVEDOR = null;
            }

            if (TITULO.equals("")
                    || MONTO.equals("")) {
                JOptionPane.showMessageDialog(this, "Uno o mas campos estan vacios, favor de llenarlos.", "Mi Taller", JOptionPane.INFORMATION_MESSAGE);
            } else {
                pst.setString(1, "0");
                pst.setString(2, titulo.getText().trim());
                pst.setString(3, ((JTextField) fecha.getDateEditor().getUiComponent()).getText());
                pst.setString(4, DESCRIPCION);
                pst.setString(5, PROVEDOR);
                pst.setString(6, monto.getText().trim());
                pst.executeUpdate();

                JOptionPane.showMessageDialog(null, "Egreso Guardado", "Mi Taller", JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
                new A単adirEgreso().setVisible(true);
            }
        } catch (Exception e) {
            System.err.print(e);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_BTN_AgregarMouseClicked


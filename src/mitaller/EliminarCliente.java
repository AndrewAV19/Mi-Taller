    private void BTN_AgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BTN_AgregarMouseClicked
        try {

            String clienteSeleccionado = (String) rfc.getSelectedItem();
            String[] datosCliente = clienteSeleccionado.split(" - ");
            String RFC = datosCliente[0];

            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/tecnoauto", "root", "");
            PreparedStatement pst = cn.prepareStatement("select * from clientes where rfc = ?");
            pst.setString(1, RFC);

            ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                rfc.setEnabled(false);
                buscarCliente1.setVisible(false);
                buscar_clientetxt.setVisible(false);
                //rfc.setEditable(false);
                nombre.setVisible(true);
                nombre.setEditable(false);
                //apellidos.setVisible(true);
                direccion.setVisible(true);
                direccion.setEditable(false);
                correo.setVisible(true);
                correo.setEditable(false);
                telefono.setVisible(true);
                telefono.setEditable(false);
                telefono2.setVisible(true);
                telefono2.setEditable(false);
                nombre_txt.setVisible(true);
                //apellido_txt.setVisible(true);
                direccion_txt.setVisible(true);
                correo_txt.setVisible(true);
                telefono_txt.setVisible(true);
                telefono2_txt.setVisible(true);
                BTN_Agregar.setVisible(false);
                BTN.setVisible(true);
                nombre.setText(rs.getString("nombre"));
                //apellidos.setText(rs.getString("apellido"));
                direccion.setText(rs.getString("direccion"));
                correo.setText(rs.getString("correo"));
                telefono.setText(rs.getString("telefono"));
                telefono2.setText(rs.getString("telefono2"));
            } else {
                JOptionPane.showMessageDialog(null, "Cliente no encontrado", "Mi Taller", JOptionPane.INFORMATION_MESSAGE);
                this.setVisible(false);
                new EliminarCliente().setVisible(true);
            }
        } catch (Exception e) {
            System.err.print(e);
        }
        // TODO add your handling code here:
    }//GEN-LAST:event_BTN_AgregarMouseClicked


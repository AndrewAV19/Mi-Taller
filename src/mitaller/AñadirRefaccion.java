    private void BTN_AgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BTN_AgregarMouseClicked
        try {
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/tecnoauto", "root", "");
            PreparedStatement pst = cn.prepareStatement("insert into hoja_refacciones values(?,?,?)");

            String ID = id.getText().trim();
            String Nombre = nombre.getText().trim();
            String Precio = precio.getText().trim();

            try {
                Connection cn1 = DriverManager.getConnection("jdbc:mysql://localhost/tecnoauto", "root", "");
                PreparedStatement pst1 = cn.prepareStatement("select id_refaccion from hoja_refacciones where id_refaccion = '" + ID + "'");
                ResultSet rs1 = pst1.executeQuery();
                if (rs1.next()) {
                    JOptionPane.showMessageDialog(null, "Duplicacion en ID", "Mi Taller", JOptionPane.INFORMATION_MESSAGE);
                    cn1.close();
                }
            } catch (SQLException e) {
            }

            if (ID.equals("") || Nombre.equals("") || Precio.equals("")) {
                JOptionPane.showMessageDialog(this, "Uno o mas campos estan vacios, favor de llenarlos\n\n Si no desea agregar el precio ingrese un 0", "Mi Taller", JOptionPane.INFORMATION_MESSAGE);
            } else {

                pst.setString(1, id.getText().trim());
                pst.setString(2, nombre.getText().trim());
                pst.setString(3, precio.getText().trim());
                pst.executeUpdate();
                LimpiarTextos();
                JOptionPane.showMessageDialog(null, "Refaccion Guardada", "Mi Taller", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (Exception e) {
            System.err.print(e);
        }

        // TODO add your handling code here:
    }//GEN-LAST:event_BTN_AgregarMouseClicked

